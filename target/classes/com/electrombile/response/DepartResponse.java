package com.electrombile.response;

public class DepartResponse {
	/**部门ID**/
	private Integer departmentId;
	/**部门名称**/
	private String departmentName;
	/**部门编号**/
	private String departmentNo;
	/**部门状态**/
	private String departmentStatus;
	
	/**
	 * @return the departmentId
	 */
	public Integer getDepartmentId() {
		return departmentId;
	}
	/**
	 * @param departmentId the departmentId to set
	 */
	public void setDepartmentId(Integer departmentId) {
		this.departmentId = departmentId;
	}
	/**
	 * @return the departmentName
	 */
	public String getDepartmentName() {
		return departmentName;
	}
	/**
	 * @param departmentName the departmentName to set
	 */
	public void setDepartmentName(String departmentName) {
		this.departmentName = departmentName;
	}
	/**
	 * @return the departmentNo
	 */
	public String getDepartmentNo() {
		return departmentNo;
	}
	/**
	 * @param departmentNo the departmentNo to set
	 */
	public void setDepartmentNo(String departmentNo) {
		this.departmentNo = departmentNo;
	}
	/**
	 * @return the departmentStatus
	 */
	public String getDepartmentStatus() {
		return departmentStatus;
	}
	/**
	 * @param departmentStatus the departmentStatus to set
	 */
	public void setDepartmentStatus(String departmentStatus) {
		this.departmentStatus = departmentStatus;
	}
	
}
